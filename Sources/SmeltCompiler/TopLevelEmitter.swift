                weightEntries: weightEntries,
                weightsSlot: weightsSlot,
                groupSize: groupSize,
                ffnDownFp32Slot: ffnDownFp32Slot,
                projectionActivationPlanesSlot: projectionActivationPlanesSlot,
                projectionActivationScalesSlot: projectionActivationScalesSlot,
                kernelPlan: kernelPlan,
                traceMode: traceMode,
                emitter: &emitter
            )
        case .geglu:
            return try emitGeGLU(
                layerIndex: layerIndex,
                config: config,
                weightEntries: weightEntries,
                weightsSlot: weightsSlot,
                groupSize: groupSize,
                ffnDownFp32Slot: ffnDownFp32Slot,
                kernelPlan: kernelPlan,
                emitter: &emitter
            )
        }
    }

    /// Emit SwiGLU FFN: fused gate+up+SwiGLU, then down proj.
    private static func emitSwiGLU(
        layerIndex: Int,
        config: SmeltConfig,
        weightEntries: [String: SmeltWeightEntry],
        weightsSlot: Int,
        groupSize: Int,
        ffnDownFp32Slot: Int,
        projectionActivationPlanesSlot: Int,
        projectionActivationScalesSlot: Int,
        kernelPlan: SmeltKernelPlan,
        traceMode: SmeltTraceMode,
        emitter: inout SmeltCodeEmitter
    ) throws -> [String] {
        let hidden = config.hiddenSize
        let ffnDim = config.ffnDim(for: layerIndex)
        let normOutSlot = SmeltFixedSlot.normOutBuf.rawValue
        var lines: [String] = []

        let gateName = SmeltKernelConsumerNaming.ffnGateWeight(layerIndex: layerIndex)
        let upName = SmeltKernelConsumerNaming.ffnUpWeight(layerIndex: layerIndex)
        let gateEntry = try requireWeight(gateName, from: weightEntries)
        let upEntry = try requireWeight(upName, from: weightEntries)

        let ffnInputBank = config.projectionBank(
            source: .ffnInput,
            containing: [.ffnGate, .ffnUp]
        )
        var signedViewLines: [String]?
        if let activationView = ffnInputBank?.activationView,
           ffnInputBank?.usesActivationView(at: layerIndex) == true,
           projectionActivationPlanesSlot >= 0,
           projectionActivationScalesSlot >= 0 {
            signedViewLines = try emitter.emitSignedBitplaneGateUpSwigluIfPossible(
                view: activationView,
                gateEntry: gateEntry,
                upEntry: upEntry,
                weightsSlot: weightsSlot,
                inputSlot: normOutSlot,
                planesSlot: projectionActivationPlanesSlot,
                activationScalesSlot: projectionActivationScalesSlot,
                outputSlot: SmeltFixedSlot.ffnIntBuf.rawValue,
                rows: ffnDim,
                cols: hidden,
                producerComment: "CAM ffn-input activation view",
                consumerComment: "FFN low-bit gate+up+SwiGLU (signed g128)"
            )
            if signedViewLines == nil {
                signedViewLines = try emitter
                    .emitSignedBitplaneStagedGateUpSwigluIfPossible(
                        view: activationView,
                        gateEntry: gateEntry,
                        upEntry: upEntry,
                        weightsSlot: weightsSlot,
                        inputSlot: normOutSlot,
                        planesSlot: projectionActivationPlanesSlot,
                        activationScalesSlot: projectionActivationScalesSlot,
                        gateOutputSlot: SmeltFixedSlot.ffnGateBuf.rawValue,
                        upOutputSlot: SmeltFixedSlot.ffnUpBuf.rawValue,
                        outputSlot: SmeltFixedSlot.ffnIntBuf.rawValue,
                        rows: ffnDim,
                        cols: hidden,
                        producerComment: "CAM ffn-input activation view",
                        consumerComment: "FFN staged low-bit gate/up projections"
                    )
            }
        }

        // Family via the ONE gateway: the fused gate+up+SwiGLU kernel is used iff BOTH weights
        // authorize the same fused-SwiGLU family (u4_lut/affine_u4); else the separate
        // (gateway-routed) matvecs below.
        let gateUpFamily = emitter.bothFusedFamily(
            gateEntry, upEntry, shape: .gemv, fusion: .gateUpSwiglu)
        let exactTernaryBankLines = gateUpFamily == .ternary2
            ? try emitter.emitSignedTernaryAffineBankGateUpSwigluIfPossible(
                gateEntry: gateEntry,
                upEntry: upEntry,
                weightsSlot: weightsSlot,
                inputSlot: normOutSlot,
                gateOutputSlot: SmeltFixedSlot.ffnGateBuf.rawValue,
                upOutputSlot: SmeltFixedSlot.ffnUpBuf.rawValue,
                outputSlot: SmeltFixedSlot.ffnIntBuf.rawValue,
                rows: ffnDim,
                cols: hidden,
                comment: "CAM ffn-input exact ternary projection bank"
            )
            : nil
        if let signedViewLines {
            lines += signedViewLines
            emitter.recordTraceMarker(
                label: "L\(layerIndex).ffn_int",
                bufferSlot: SmeltFixedSlot.ffnIntBuf.rawValue
            )
        } else if gateUpFamily == .lutU4, let gateLutOff = gateEntry.lutOffset, let upLutOff = upEntry.lutOffset
        {
            // Fused: gate_proj + up_proj + SwiGLU in one dispatch → ffnIntBuf
            lines += try emitter.emitFusedGateUpSwiglu(
                gateIndicesSlot: weightsSlot, gateIndicesOffset: gateEntry.offset,
                gateLutSlot: weightsSlot, gateLutOffset: gateLutOff,
                upIndicesSlot: weightsSlot, upIndicesOffset: upEntry.offset,
                upLutSlot: weightsSlot, upLutOffset: upLutOff,
                inputSlot: normOutSlot,
                outputSlot: SmeltFixedSlot.ffnIntBuf.rawValue,
                rows: ffnDim, cols: hidden, groupSize: groupSize,
                comment: "FFN fused gate+up+SwiGLU"
            )
            emitter.recordTraceMarker(
                label: "L\(layerIndex).ffn_int",
                bufferSlot: SmeltFixedSlot.ffnIntBuf.rawValue
            )
        } else if gateUpFamily == .binary1 {
            lines += try emitter.emitSignedBinaryGateUpSwiglu(
                gateEntry: gateEntry,
                upEntry: upEntry,
                weightsSlot: weightsSlot,
                inputBinding: SmeltBufferBinding(slot: normOutSlot, index: 4),
                outputBinding: SmeltBufferBinding(
                    slot: SmeltFixedSlot.ffnIntBuf.rawValue, index: 5),
                rows: ffnDim,
                cols: hidden,
                comment: "FFN fused gate+up+SwiGLU (binary g128)"
            )
            emitter.recordTraceMarker(
                label: "L\(layerIndex).ffn_int",
                bufferSlot: SmeltFixedSlot.ffnIntBuf.rawValue
            )
        } else if let exactTernaryBankLines {
            lines += exactTernaryBankLines
            emitter.recordTraceMarker(
                label: "L\(layerIndex).ffn_int",
                bufferSlot: SmeltFixedSlot.ffnIntBuf.rawValue
            )
        } else if gateUpFamily == .ternary2 {
            lines += try emitter.emitSignedTernaryAffineGateUpSwiglu(
                gateEntry: gateEntry,
                upEntry: upEntry,
                weightsSlot: weightsSlot,
                inputBinding: SmeltBufferBinding(slot: normOutSlot, index: 6),
                outputBinding: SmeltBufferBinding(
                    slot: SmeltFixedSlot.ffnIntBuf.rawValue, index: 7),
                rows: ffnDim,
                cols: hidden,
                comment: "FFN fused gate+up+SwiGLU (MLX-exact ternary g128)"
            )
            emitter.recordTraceMarker(
                label: "L\(layerIndex).ffn_int",
                bufferSlot: SmeltFixedSlot.ffnIntBuf.rawValue
            )
        } else if gateUpFamily == .affineU4,
                  let gateScales = gateEntry.scalesOffset, let gateBiases = gateEntry.biasesOffset,
                  let upScales = upEntry.scalesOffset, let upBiases = upEntry.biasesOffset,
                  // Entry-resolved group sizes must agree (per-tensor group
                  // sizes are possible) — the fused kernel bakes ONE group size.
                  (gateEntry.groupSize ?? groupSize) == (upEntry.groupSize ?? groupSize)
        {
            // Fused affine: gate_proj + up_proj + SwiGLU in one dispatch → ffnIntBuf
            lines += try emitter.emitFusedAffineGateUpSwiglu(
                gateWeightsSlot: weightsSlot, gateWeightsOffset: gateEntry.offset,
                gateScalesSlot: weightsSlot, gateScalesOffset: gateScales,
                gateBiasesSlot: weightsSlot, gateBiasesOffset: gateBiases,
                upWeightsSlot: weightsSlot, upWeightsOffset: upEntry.offset,
                upScalesSlot: weightsSlot, upScalesOffset: upScales,
                upBiasesSlot: weightsSlot, upBiasesOffset: upBiases,
                inputSlot: normOutSlot,
                outputSlot: SmeltFixedSlot.ffnIntBuf.rawValue,
                rows: ffnDim, cols: hidden,
                groupSize: gateEntry.groupSize ?? groupSize,
                comment: "FFN fused gate+up+SwiGLU (affine)"
            )
            emitter.recordTraceMarker(
                label: "L\(layerIndex).ffn_int",
                bufferSlot: SmeltFixedSlot.ffnIntBuf.rawValue
            )
        } else {
            // Fallback: separate gate, up, swiglu dispatches
            lines += try emitter.emitMatvec(
                weightEntry: gateEntry,
                weightsSlot: weightsSlot,
                inputSlot: normOutSlot,
                outputSlot: SmeltFixedSlot.ffnGateBuf.rawValue,
                rows: ffnDim, cols: hidden, groupSize: groupSize,
                comment: "FFN gate projection"
            )
            emitter.recordTraceMarker(
                label: "L\(layerIndex).ffn_gate",
                bufferSlot: SmeltFixedSlot.ffnGateBuf.rawValue
            )
            lines += try emitter.emitMatvec(
                weightEntry: upEntry,
                weightsSlot: weightsSlot,
                inputSlot: normOutSlot,
                outputSlot: SmeltFixedSlot.ffnUpBuf.rawValue,
                rows: ffnDim, cols: hidden, groupSize: groupSize,
                comment: "FFN up projection"
            )
            emitter.recordTraceMarker(
                label: "L\(layerIndex).ffn_up",
                bufferSlot: SmeltFixedSlot.ffnUpBuf.rawValue
            )
            lines += try emitter.emit(SmeltDispatch(
                pipeline: .swigluFused,
                buffers: [
                    SmeltBufferBinding(
                        slot: SmeltFixedSlot.ffnGateBuf.rawValue, index: 0
                    ),
                    SmeltBufferBinding(
                        slot: SmeltFixedSlot.ffnUpBuf.rawValue, index: 1
                    ),
                    SmeltBufferBinding(
                        slot: SmeltFixedSlot.ffnIntBuf.rawValue, index: 2
                    ),
                ],
                constants: [
                    SmeltConstantBinding(
                        expression: "\(ffnDim)", type: .uint32, index: 3
                    ),
                ],
                dispatch: .threads(
                    width: ffnDim, height: 1, depth: 1,
                    tgWidth: min(ffnDim, 1024), tgHeight: 1, tgDepth: 1
                ),
                comment: "SwiGLU fused"
            ))
            emitter.recordTraceMarker(
                label: "L\(layerIndex).ffn_int",
                bufferSlot: SmeltFixedSlot.ffnIntBuf.rawValue
            )
        }

        // Down projection: values can exceed FP16 range before the following RMSNorm.
        let downName = SmeltKernelConsumerNaming.ffnDownWeight(layerIndex: layerIndex)
        let downEntry = try requireWeight(downName, from: weightEntries)
        let downOutputSlot = ffnDownFp32Slot >= 0 && downEntry.dtype == .fp16
            ? ffnDownFp32Slot
            : SmeltFixedSlot.ffnDownBuf.rawValue
        var lowBitDownLines: [String]?
        if let downBank = config.projectionBank(
            source: .ffnIntermediate,
            containing: [.ffnDown]
        ),
           let activationView = downBank.activationView,
           downBank.usesActivationView(at: layerIndex),
           projectionActivationPlanesSlot >= 0,
           projectionActivationScalesSlot >= 0,
           downEntry.dtype == .binary1 || downEntry.dtype == .ternary2 {
            lines += try emitter.emitSignedActivationView(
                activationView,
                inputSlot: SmeltFixedSlot.ffnIntBuf.rawValue,
                planesSlot: projectionActivationPlanesSlot,
                scalesSlot: projectionActivationScalesSlot,
                cols: ffnDim,
                comment: "CAM ffn-intermediate activation view"
            )
            lowBitDownLines = try emitter.emitSignedBitplaneMatvecIfPossible(
                view: activationView,
                weightEntry: downEntry,
                weightsSlot: weightsSlot,
                planesSlot: projectionActivationPlanesSlot,
                activationScalesSlot: projectionActivationScalesSlot,
                outputSlot: downOutputSlot,
                rows: hidden,
                cols: ffnDim,
                comment: "FFN low-bit down projection"
            )
        }
        if let lowBitDownLines {
            lines += lowBitDownLines
        } else if ffnDownFp32Slot >= 0 && downEntry.dtype == .fp16 {
            lines += try emitter.emitFP16MatvecFP32Out(
                weightSlot: weightsSlot, weightOffset: downEntry.offset,
                inputSlot: SmeltFixedSlot.ffnIntBuf.rawValue,
                outputSlot: downOutputSlot,
                rows: hidden, cols: ffnDim,
                comment: "FFN down projection (FP32 output)"
            )
        } else {
            lines += try emitter.emitMatvec(
                weightEntry: downEntry,
                weightsSlot: weightsSlot,
                inputSlot: SmeltFixedSlot.ffnIntBuf.rawValue,
                outputSlot: downOutputSlot,
                rows: hidden, cols: ffnDim, groupSize: groupSize,
                comment: "FFN down projection",
                plannedKernelRoute: kernelPlan.route(
                    kind: .ffnDownResidualDecode,
                    context: SmeltKernelLayerConsumerContext(
                        config: config,
                        layerIndex: layerIndex,
                        groupSize: downEntry.groupSize ?? groupSize
                    )
                )
            )
        }
        if !traceMode.usesStrippedOptimizations {
            emitter.recordTraceMarker(
                label: "L\(layerIndex).ffn_down",
                bufferSlot: downOutputSlot
            )
        }

        return lines
    }

    private static func emitGeGLU(
        layerIndex: Int,
        config: SmeltConfig,
        weightEntries: [String: SmeltWeightEntry],
        weightsSlot: Int,
        groupSize: Int,
        ffnDownFp32Slot: Int,
        kernelPlan: SmeltKernelPlan,
        emitter: inout SmeltCodeEmitter
    ) throws -> [String] {
        let hidden = config.hiddenSize
        let ffnDim = config.ffnDim(for: layerIndex)
        let normOutSlot = SmeltFixedSlot.normOutBuf.rawValue
        var lines: [String] = []

        let gateName = SmeltKernelConsumerNaming.ffnGateWeight(layerIndex: layerIndex)
        let upName = SmeltKernelConsumerNaming.ffnUpWeight(layerIndex: layerIndex)
        let downName = SmeltKernelConsumerNaming.ffnDownWeight(layerIndex: layerIndex)
        let gateEntry = try requireWeight(gateName, from: weightEntries)
        let upEntry = try requireWeight(upName, from: weightEntries)

        // Family via the ONE gateway: the fused gate+up+GeGLU kernel is used iff BOTH weights
        // authorize the fused-GeGLU family (affine_u4); else separate matvecs.
        let gateUpFamily = emitter.bothFusedFamily(
            gateEntry, upEntry, shape: .gemv, fusion: .gateUpGeGLU)
        if gateUpFamily == .affineU4,
           let gateScales = gateEntry.scalesOffset, let gateBiases = gateEntry.biasesOffset,
           let upScales = upEntry.scalesOffset, let upBiases = upEntry.biasesOffset,
           // Entry-resolved group sizes must agree (per-tensor group
           // sizes are possible) — the fused kernel bakes ONE group size.
           (gateEntry.groupSize ?? groupSize) == (upEntry.groupSize ?? groupSize)
        {
            lines += try emitter.emitFusedAffineGateUpGeGLU(
                gateWeightsSlot: weightsSlot, gateWeightsOffset: gateEntry.offset,
                gateScalesSlot: weightsSlot, gateScalesOffset: gateScales,
                gateBiasesSlot: weightsSlot, gateBiasesOffset: gateBiases,
                upWeightsSlot: weightsSlot, upWeightsOffset: upEntry.offset,
                upScalesSlot: weightsSlot, upScalesOffset: upScales,
                upBiasesSlot: weightsSlot, upBiasesOffset: upBiases,
                inputSlot: normOutSlot,
                outputSlot: SmeltFixedSlot.ffnIntBuf.rawValue,
                rows: ffnDim, cols: hidden,
                groupSize: gateEntry.groupSize ?? groupSize,
                comment: "FFN fused gate+up+GeGLU (affine)"
            )
        } else {
            lines += try emitter.emitMatvec(
                weightEntry: gateEntry,
                weightsSlot: weightsSlot,
                inputSlot: normOutSlot,
                outputSlot: SmeltFixedSlot.ffnGateBuf.rawValue,
                rows: ffnDim, cols: hidden, groupSize: groupSize,
                comment: "FFN gate projection"
            )
            emitter.recordTraceMarker(
                label: "L\(layerIndex).ffn_gate",
                bufferSlot: SmeltFixedSlot.ffnGateBuf.rawValue
            )
            lines += try emitter.emitMatvec(
                weightEntry: upEntry,
                weightsSlot: weightsSlot,
                inputSlot: normOutSlot,
                outputSlot: SmeltFixedSlot.ffnUpBuf.rawValue,
                rows: ffnDim, cols: hidden, groupSize: groupSize,
                comment: "FFN up projection"
            )
            emitter.recordTraceMarker(
                label: "L\(layerIndex).ffn_up",
                bufferSlot: SmeltFixedSlot.ffnUpBuf.rawValue
            )
            lines += try emitter.emitGeGLU(
                gateSlot: SmeltFixedSlot.ffnGateBuf.rawValue,
                upSlot: SmeltFixedSlot.ffnUpBuf.rawValue,
                outputSlot: SmeltFixedSlot.ffnIntBuf.rawValue,
                count: ffnDim,
                comment: "GeGLU fused"
            )
        }
        emitter.recordTraceMarker(
            label: "L\(layerIndex).ffn_int",
            bufferSlot: SmeltFixedSlot.ffnIntBuf.rawValue
        )
        let downEntry = try requireWeight(downName, from: weightEntries)
        let downOutputSlot = ffnDownFp32Slot >= 0 && downEntry.dtype == .fp16
            ? ffnDownFp32Slot
            : SmeltFixedSlot.ffnDownBuf.rawValue
        if ffnDownFp32Slot >= 0 && downEntry.dtype == .fp16 {
            lines += try emitter.emitFP16MatvecFP32Out(
                weightSlot: weightsSlot, weightOffset: downEntry.offset,
                inputSlot: SmeltFixedSlot.ffnIntBuf.rawValue,
                outputSlot: downOutputSlot,
                rows: hidden, cols: ffnDim,
                comment: "FFN down projection (FP32 output)"
            )
        } else {
            lines += try emitter.emitMatvec(
                weightEntry: downEntry,
                weightsSlot: weightsSlot,
                inputSlot: SmeltFixedSlot.ffnIntBuf.rawValue,
                outputSlot: downOutputSlot,
                rows: hidden, cols: ffnDim, groupSize: groupSize,
                comment: "FFN down projection",
                plannedKernelRoute: kernelPlan.route(
                    kind: .ffnDownResidualDecode,
                    context: SmeltKernelLayerConsumerContext(
                        config: config,
                        layerIndex: layerIndex,
                        groupSize: downEntry.groupSize ?? groupSize
                    )
                )
            )
        }
        emitter.recordTraceMarker(
            label: "L\(layerIndex).ffn_down",
            bufferSlot: downOutputSlot
        )
        return lines
    }

    private static func emitStrippedNormScaleGeGLUFFNIfPossible(
        layerIndex: Int,
        config: SmeltConfig,
        weightEntries: [String: SmeltWeightEntry],
        weightsSlot: Int,
        groupSize: Int,
        preFeedforwardNormOffset: UInt64,
        ffnDownFp32Slot: Int,
        emitter: inout SmeltCodeEmitter
    ) throws -> [String]? {
        guard config.ffn.activation == .geglu else { return nil }

        // Current norm-scale GeGLU decode fusion is not exact against the
        // staged quantized contract on real prompts. Keep the stripped
        // exact path on staged pre-FFN RMSNorm + affine GeGLU until this
        // fusion can be proven exact end-to-end.
        return nil
    }

    private static func emitPerLayerInputsSetup(
        config: SmeltConfig,
        weightEntries: [String: SmeltWeightEntry],
        weightsSlot: Int,
        tokenIdSlot: Int,
        groupSize: Int,
        quantizeEmbedding: Bool,
        emitter: inout SmeltCodeEmitter
    ) throws -> [String] {
        let hidden = config.hiddenSize
        let perLayerHidden = config.hiddenSizePerLayerInput
        let perLayerTotalDim = config.numLayers * perLayerHidden
        let perLayerInputsSlot = SmeltFixedSlot.perLayerInputsBuf.rawValue
        let perLayerScratchSlot = SmeltFixedSlot.perLayerScratchBuf.rawValue
        var lines: [String] = []

        let perLayerEmbedEntry = try requireWeight(
            SmeltCanonicalTensorNames.embedTokensPerLayer, from: weightEntries
        )
        if quantizeEmbedding, perLayerEmbedEntry.dtype == .u4Lut, let lutOff = perLayerEmbedEntry.lutOffset {
            lines += try emitter.emitLUTEmbeddingGather(
                indicesSlot: weightsSlot, indicesOffset: perLayerEmbedEntry.offset,
                lutSlot: weightsSlot, lutOffset: lutOff,
                tokenIdSlot: tokenIdSlot,
                outputSlot: perLayerInputsSlot,
                hiddenSize: perLayerTotalDim, groupSize: groupSize,
                comment: "Per-layer token inputs (quantized gather)"
            )
        } else if quantizeEmbedding,
                  perLayerEmbedEntry.dtype == .affineU4,
                  let scalesOff = perLayerEmbedEntry.scalesOffset,
                  let biasesOff = perLayerEmbedEntry.biasesOffset {
            lines += try emitter.emitAffineEmbeddingGather(
                weightsSlot: weightsSlot, weightsOffset: perLayerEmbedEntry.offset,
                scalesSlot: weightsSlot, scalesOffset: scalesOff,
                biasesSlot: weightsSlot, biasesOffset: biasesOff,
                tokenIdSlot: tokenIdSlot,
                outputSlot: perLayerInputsSlot,
                hiddenSize: perLayerTotalDim, groupSize: groupSize,
                comment: "Per-layer token inputs (affine quantized gather)"
            )
        } else if perLayerEmbedEntry.dtype == .binary1
                    || perLayerEmbedEntry.dtype == .ternary2 {
            lines += try emitter.emitSignedEmbeddingGather(
                weightEntry: perLayerEmbedEntry,
                weightsSlot: weightsSlot,
                tokenIdSlot: tokenIdSlot,
                outputSlot: perLayerInputsSlot,
                hiddenSize: perLayerTotalDim,
                comment: "Per-layer token inputs (native signed gather)"
            )
        } else if perLayerEmbedEntry.dtype == .turboQuantH,
                  let codebookOff = perLayerEmbedEntry.codebookOffset,
                  let codesPerRow = perLayerEmbedEntry.packedRowStride {
            lines += try emitter.emitTurboQuantHEmbeddingGather(
                codesSlot: weightsSlot, codesOffset: perLayerEmbedEntry.offset,
                codebookSlot: weightsSlot, codebookOffset: codebookOff,
                tokenIdSlot: tokenIdSlot,
                outputSlot: perLayerInputsSlot,
                hiddenSize: perLayerTotalDim, codesPerRow: codesPerRow,
                comment: "Per-layer token inputs (turbo_quant_h gather)"
            )
        } else {
            lines += try emitter.emitEmbeddingGather(
                weightSlot: weightsSlot, weightOffset: perLayerEmbedEntry.offset,
                tokenIdSlot: tokenIdSlot,
                outputSlot: perLayerInputsSlot,
                hiddenSize: perLayerTotalDim,
                comment: "Per-layer token inputs"
            )
        }
        lines += try emitter.emitScalarMul(
            inputSlot: perLayerInputsSlot,
            outputSlot: perLayerInputsSlot,
            scalar: Float(perLayerHidden).squareRoot(),
            count: perLayerTotalDim,
            comment: "Per-layer token input scale"
        )

        lines += try emitter.emitMatvec(
            weightEntry: try requireWeight("per_layer_model_projection_weight", from: weightEntries),
            weightsSlot: weightsSlot,
            inputSlot: SmeltFixedSlot.hiddenA.rawValue,
            outputSlot: perLayerScratchSlot,
            rows: perLayerTotalDim, cols: hidden, groupSize: groupSize,
            comment: "Per-layer model projection"
        )
        lines += try emitter.emitScalarMul(
            inputSlot: perLayerScratchSlot,
            outputSlot: perLayerScratchSlot,
            scalar: 1.0 / Float(hidden).squareRoot(),
            count: perLayerTotalDim,
            comment: "Per-layer model projection scale"
        )
        lines += try AttentionPlugin.emitPerHeadNorm(
            emitter: &emitter,
            dataSlot: perLayerScratchSlot,
            numHeads: config.numLayers,
            weightName: "per_layer_projection_norm_weight",
            weightsSlot: weightsSlot,
            weightEntries: weightEntries,
            headDim: perLayerHidden,
            eps: config.rmsEps,
            mode: .weight,
            comment: "Per-layer projection RMS norm"
        )
        lines += try emitter.emitElementwiseAdd(
            inputASlot: perLayerScratchSlot,
            inputBSlot: perLayerInputsSlot,
            outputSlot: perLayerInputsSlot,
            count: perLayerTotalDim,
            comment: "Combine per-layer inputs"
        )
        lines += try emitter.emitScalarMul(
            inputSlot: perLayerInputsSlot,
            outputSlot: perLayerInputsSlot,
            scalar: 0.70710677,
            count: perLayerTotalDim,
            comment: "Per-layer input scale"
        )
        emitter.recordTraceMarker(
            label: "per_layer_inputs",
            bufferSlot: perLayerInputsSlot
        )
        return lines
    }

    private static func emitPerLayerResidualBranch(
        layerIndex: Int,
        config: SmeltConfig,
        weightEntries: [String: SmeltWeightEntry],
        weightsSlot: Int,
        groupSize: Int,
        emitter: inout SmeltCodeEmitter,
        traceMode: SmeltTraceMode
    ) throws -> [String] {
        let hidden = config.hiddenSize
        let perLayerHidden = config.hiddenSizePerLayerInput
        let perLayerOffset = UInt64(layerIndex * perLayerHidden * 2)
        var lines: [String] = []

        lines += try emitter.emitMatvecVar(
            weightEntry: try requireWeight("layers_\(layerIndex)_per_layer_input_gate_weight", from: weightEntries),
            weightsSlot: weightsSlot,
            inputSlotVar: "alt",
            outputSlot: SmeltFixedSlot.ffnGateBuf.rawValue,
            rows: perLayerHidden, cols: hidden, groupSize: groupSize,
            comment: "Per-layer input gate"
        )
        emitter.recordTraceMarker(
            label: "L\(layerIndex).per_layer_gate",
            bufferSlot: SmeltFixedSlot.ffnGateBuf.rawValue
        )
        lines += try emitter.emitGeGLU(
            gateSlot: SmeltFixedSlot.ffnGateBuf.rawValue,
            upSlot: SmeltFixedSlot.perLayerInputsBuf.rawValue,
            upOffset: perLayerOffset,
            outputSlot: SmeltFixedSlot.ffnIntBuf.rawValue,
            count: perLayerHidden,
            comment: "Per-layer GELU x residual input"
        )
        emitter.recordTraceMarker(
            label: "L\(layerIndex).per_layer_int",
            bufferSlot: SmeltFixedSlot.ffnIntBuf.rawValue
        )
        lines += try emitter.emitMatvec(
            weightEntry: try requireWeight("layers_\(layerIndex)_per_layer_projection_weight", from: weightEntries),
            weightsSlot: weightsSlot,
            inputSlot: SmeltFixedSlot.ffnIntBuf.rawValue,
            outputSlot: SmeltFixedSlot.normOutBuf.rawValue,
            rows: hidden, cols: perLayerHidden, groupSize: groupSize,
            comment: "Per-layer projection"
        )
        emitter.recordTraceMarker(
            label: "L\(layerIndex).per_layer_proj",
            bufferSlot: SmeltFixedSlot.normOutBuf.rawValue
        )
        let postPerLayerNormOffset = try requireWeight(
            "layers_\(layerIndex)_post_per_layer_input_norm_weight",
            from: weightEntries
        ).offset
        lines += try emitter.emitRMSNorm1PW(
            inputSlot: SmeltFixedSlot.normOutBuf.rawValue,
            weightSlot: weightsSlot,
            weightOffset: postPerLayerNormOffset,
            outputSlot: SmeltFixedSlot.residualBuf.rawValue,
            dim: hidden, eps: config.rmsEps,
            comment: "Post per-layer input norm"
        )
        emitter.recordTraceMarker(
            label: "L\(layerIndex).post_per_layer_norm",
            bufferSlot: SmeltFixedSlot.residualBuf.rawValue
        )
        lines += try emitter.emitElementwiseAddVar(
            inputAVar: "alt",
            inputBSlot: SmeltFixedSlot.residualBuf.rawValue,
            outputVar: "alt", count: hidden,
            comment: "Per-layer residual add"
        )
        emitter.recordTraceMarker(
            label: "L\(layerIndex).post_per_layer_residual",
            bufferSlot: SmeltFixedSlot.hiddenA.rawValue
        )
        return lines
    }
